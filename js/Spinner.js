function Spinner(overlay, spinner) {
    this.overlay = overlay;
    this.spinner = spinner;
}

Spinner.prototype.showSpinner = function () {
    this.style.display = 'block';
    this.style.display = 'block';
};

Spinner.prototype.hideSpinner = function () {
    this.style.display = 'none';
    this.style.display = 'none';
};
